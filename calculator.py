a = 20
b = 10
add = a+b
sub = a-b
mul = a*b
div = a/b
rem = a % b
floor = a//b
expo = a**2

print("The addition of ", a, "+", b, "is:", add)
print("The subtraction of ", a, "-", b, "is:", sub)
print("The product of ", a, "*", b, "is:", mul)
print("The quotient of ", a, "/", b, "is:", div)
print("The remainder of ", a, "/", b, "is:", rem)
print("The floored division(only integer value) of ", a, "//", b, "is:", floor)
print("The power raised to two for ", a, "is:", expo)
